#include <iostream>
#include<string.h>

using namespace std;

int main()
{

    int i,n,c=0,d=0,value;
    cin>>n;
    for(i=0;i<n;i++){
        cin>>value;
        if(value>=10 && value<=20)
            c++;
        else
            d++;
    }
    cout<<c<<" in"<<endl;
    cout<<d<<" out"<<endl;

    return 0;
}
